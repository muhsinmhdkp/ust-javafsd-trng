package com.ust;

public class Switch3 {
	public static void main( String args[]) {
		int expression = 2;
		switch (expression) {
		case 1 :
			System.out.println("case 1");
		case 2 :
			System.out.println("case 2");
		case 3 :
			System.out.println("case 3");
		default :
			System.out.println("Default case");
		}
	}

}
