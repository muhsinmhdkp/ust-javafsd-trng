package com.ust;

public class Ifelseif {
	public static void main ( String args[]) {
		int number = 0;
		if (number > 0) {
			System.out.println("The number is postive");
		}
		else if (number <0) {
			System.out.println("The number is negtive");
		}
		else {
			System.out.println("The number is zero");
		}
	}

}
